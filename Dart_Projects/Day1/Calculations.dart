void main(){
  double num1=15;
  double num2=12;
  double result=0;
  result=num1+num2;
  print('$num1 + $num2 = $result');
  result=num1-num2;
  print('$num1 - $num2 = $result');
  result=num1*num2;
  print('$num1 * $num2 = $result');
  result=num1/num2;
  print('$num1 / $num2 = $result');
  result=num1%num2;
  print('$num1 % $num2 = $result');
}